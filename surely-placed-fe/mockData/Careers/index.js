export const getCareerSlug = (title = '') =>
  title
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

export const getCareerBySlug = (slug = '') =>
  careersOpenings.find((job) => getCareerSlug(job.title) === slug);
